def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the combined string.

    Args:
        str1 (str): first string to concatenate.
        str2 (str): second string to concatenate.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
